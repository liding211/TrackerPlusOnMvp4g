package com.github.gwtbootstrap.timepicker.client.ui.base;

/**
 * Define a element that will have the showInputs property
 *
 * @author Carlos Alexandro Becker
 * @author Joshua Godi
 * @since 2.3.2.0
 */
public interface HasShowInputs {

    /**
     * Sets the showInputs property component
     *
     * @param showInputs: if true shows the inputs on the component
     */
    public void setShowInputs(boolean showInputs);

}
