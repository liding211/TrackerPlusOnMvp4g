package com.github.gwtbootstrap.timepicker.client.ui.base;

/**
 * Define a element that will have the modalBackdrop property
 *
 * @author Carlos Alexandro Becker
 * @author Joshua Godi
 * @since 2.3.2.0
 */
public interface HasModalBackdrop {

    /**
     * Sets the hasModalBackdrop property component
     *
     * @param modalBackdrop: if true shows a backdrop modal for the component
     */
    public void setModalBackdrop(boolean modalBackdrop);

}
